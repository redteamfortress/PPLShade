#pragma once
#include <windows.h>

#define _XK 0x4B

static __forceinline void _xd(char* d, const unsigned char* s, int n) {
    for (int i = 0; i < n; i++) d[i] = s[i] ^ _XK;
    d[n] = '\0';
}
static __forceinline void _xdW(wchar_t* d, const unsigned char* s, int n) {
    for (int i = 0; i < n; i++) d[i] = (wchar_t)(s[i] ^ _XK);
    d[n] = L'\0';
}

